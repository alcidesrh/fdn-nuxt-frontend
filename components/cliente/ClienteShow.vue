<template>
  <div class="flex items-center justify-between">
    <nuxt-link
      :to="{ name: 'clientes' }"
      class="text-blue-600 hover:text-blue-800"
    >
      &lt; Back to list
    </nuxt-link>

    <div>
      <nuxt-link
        v-if="item"
        :to="{ name: 'clientes-id-edit', params: { id: getIdFromIri(item['@id']) } }"
        class="px-6 py-2 mr-2 bg-green-600 text-white text-xs rounded shadow-md hover:bg-green-700"
      >
        Edit
      </nuxt-link>
      <button
        class="px-6 py-2 bg-red-600 text-white text-xs rounded shadow-md hover:bg-red-700"
        @click="deleteItem"
      >
        Delete
      </button>
    </div>
  </div>

  <h1 class="text-3xl my-4">Show Cliente {{ item?.["@id"] }}</h1>

  <div
    v-if="isLoading"
    class="bg-blue-100 rounded py-4 px-4 text-blue-700 text-sm"
    role="status"
  >
    Loading...
  </div>

  <div
    v-if="error || deleteError"
    class="bg-red-100 rounded py-4 px-4 my-2 text-red-700 text-sm"
    role="alert"
  >
    {{ error || deleteError }}
  </div>

  <div v-if="item" class="overflow-x-auto">
    <table class="min-w-full">
      <thead class="border-b">
        <tr>
          <th scope="col" class="text-sm font-medium px-6 py-4 text-left">
            Field
          </th>
          <th scope="col" class="text-sm font-medium px-6 py-4 text-left">
            Value
          </th>
        </tr>
      </thead>
      <tbody>
        <tr class="border-b">
          <th
            class="text-sm font-medium px-6 py-4 text-left capitalize"
            scope="row"
          >
            nit
          </th>
          <td class="px-6 py-4 whitespace-nowrap text-sm">
            {{ item.nit }}
                    </td>
        </tr>
        <tr class="border-b">
          <th
            class="text-sm font-medium px-6 py-4 text-left capitalize"
            scope="row"
          >
            dpi
          </th>
          <td class="px-6 py-4 whitespace-nowrap text-sm">
            {{ item.dpi }}
                    </td>
        </tr>
        <tr class="border-b">
          <th
            class="text-sm font-medium px-6 py-4 text-left capitalize"
            scope="row"
          >
            apellido
          </th>
          <td class="px-6 py-4 whitespace-nowrap text-sm">
            {{ item.apellido }}
                    </td>
        </tr>
        <tr class="border-b">
          <th
            class="text-sm font-medium px-6 py-4 text-left capitalize"
            scope="row"
          >
            direccion
          </th>
          <td class="px-6 py-4 whitespace-nowrap text-sm">
            {{ item.direccion }}
                    </td>
        </tr>
        <tr class="border-b">
          <th
            class="text-sm font-medium px-6 py-4 text-left capitalize"
            scope="row"
          >
            telefono
          </th>
          <td class="px-6 py-4 whitespace-nowrap text-sm">
            {{ item.telefono }}
                    </td>
        </tr>
        <tr class="border-b">
          <th
            class="text-sm font-medium px-6 py-4 text-left capitalize"
            scope="row"
          >
            correo
          </th>
          <td class="px-6 py-4 whitespace-nowrap text-sm">
            {{ item.correo }}
                    </td>
        </tr>
      </tbody>
    </table>
  </div>
</template>

<script lang="ts" setup>
import { storeToRefs } from "pinia";
import { useClienteShowStore } from "~~/stores/cliente/show";
import { useClienteDeleteStore } from "~~/stores/cliente/delete";
import { useMercureItem } from "~~/composables/mercureItem";
import { useFetchItem } from "~~/composables/api";
import { getIdFromIri } from "~~/utils/resource";
import type { Cliente } from "~~/types/cliente";

const route = useRoute();
const router = useRouter();

const clienteDeleteStore = useClienteDeleteStore();
const { error: deleteError, deleted } = storeToRefs(clienteDeleteStore);

const clienteShowStore = useClienteShowStore();

useMercureItem({
  store: clienteShowStore,
  deleteStore: clienteDeleteStore,
  redirectRouteName: "clientes",
});

const id = decodeURIComponent(route.params.id as string);
const {
  retrieved: item,
  isLoading,
  error,
  hubUrl,
} = await useFetchItem<Cliente>(`clientes/${id}`);
clienteShowStore.setData({ retrieved: item, isLoading, error, hubUrl });

async function deleteItem() {
  if (!item?.value) {
    clienteDeleteStore.setError("No item found. Please reload");
    return;
  }

  if (window.confirm("Are you sure you want to delete this cliente?")) {
    const { error } = await useDeleteItem(item.value);

    if (error.value) {
      clienteDeleteStore.setError(error.value);
      return;
    }

    clienteDeleteStore.setDeleted(item.value);
    clienteDeleteStore.setMercureDeleted(undefined);

    if (deleted) {
      router.push({ name: "clientes" });
    }
  }
}

onBeforeUnmount(() => {
  clienteShowStore.$reset();
});
</script>
